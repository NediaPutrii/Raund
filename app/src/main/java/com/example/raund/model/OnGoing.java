package com.example.raund.model;

public class OnGoing {
    public String nama_travel_og;
    public String fromDelivery_og;
    public String destinationDelivery_og;
    public String tgl_departure_og;
    public String tgl_arrival_og;
    public String jam_pergi_og;
    public String jam_datang_og;
    public String harga_og;

public OnGoing(String nama_travel_og, String fromDelivery_og, String destinationDelivery_og, String tgl_departure_og, String tgl_arrival_og, String jam_pergi_og, String jam_datang_og, String harga_og){

    this.nama_travel_og = nama_travel_og;
    this.fromDelivery_og = fromDelivery_og;
    this.destinationDelivery_og = destinationDelivery_og;
    this.tgl_departure_og= tgl_departure_og;
    this.tgl_arrival_og = tgl_arrival_og;
    this.jam_pergi_og = jam_pergi_og;
    this.jam_datang_og = jam_datang_og;
    this.harga_og = harga_og;
}

}